int n;
int K;
vector<int> a(1e5+1);
int dp[100020]; 
int rec(int level){
    if(level > n){return 0;}
    if(level == n){return 0;}

    if(dp[level] != -1){
        return dp[level];
    }
    int ans = 1e9;
    for(int i=1;i<=K;i++){
        if(level+i < n)
        ans = min(ans,rec(level+i) + abs(a[level+i]-a[level]));
    }
    if(ans == 1e9){ans = 0;}

    return dp[level] = ans;
}
void solve(){
    cin >> n;
    cin >> K;
    input(a);
    memset(dp,-1,sizeof(dp));
    cout << rec(0) << "\n";
}

