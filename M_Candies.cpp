void solve_iterative(){
    // dp[level][K] => no. of ways to distribute K coins when you are at levelth postion;
    // base case dp[0][0] = dp[0][1] = .... = dp[0][arr[level]]
    // dp[level][y] = dp[level-1][y] + dp[level][y-1] + dp[level][y-2] ... dp[level][y-arr[level]];
    // total number of ways ending at index level
    // Prefix sum optimization
    // dp[level][y] = prefix_sum[y]-prefix_sum[y-arr[level]-1] 
    // Complexity of O(N*K)*O(K)
    cin >> N >> K;
    for(int i=0;i<N;i++){
        cin >> arr[i];
    }   
    // base case
    for(int i=0;i<=arr[0];i++){
        dp[0][i] = 1;
    }
    // iterative dp
    for(int level=1;level<N;level++){
        //Prefix_sum calculation
        vector<int> prefix_sum(K+1);
        prefix_sum[0] = dp[level-1][0];
        for(int y=1;y<K+1;y++){
            prefix_sum[y] = (dp[level-1][y] + prefix_sum[y-1])%MOD;
        }

        for(int j=0;j<K+1;j++){
            if(j > arr[level]){
                dp[level][j] = (prefix_sum[j] + MOD - prefix_sum[j-arr[level]-1])%MOD;
            }else{
                dp[level][j] = (prefix_sum[j])%MOD;
            }
        }
    }

    cout << dp[N-1][K] << "\n";
}
