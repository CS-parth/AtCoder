int arr[101];
int dp[101][100010];
int N,K;
int rec(int level,int left){
    //base case
    if(level == N){
        if(left < 0){return 0;}
        if(left == 0){return 1;}
    }
    //dp check
    // if(dp[level][left] != -1){
    //     return dp[level][left];
    // }
    //transitions
    int ans = 0;
    int ways = 0; 
    for(int i=0;i<=arr[level];i++){
        ways+=rec(level+1,left-i)%MOD;
    }
    ans += ways;

    return dp[level][left] = ans%MOD;
}
