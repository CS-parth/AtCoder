int coins[4];
double dp[N][N][N];
double n;

void solve(){
    // int n;
    cin >> n;
    for(int i=1;i<=n;i++){
        int ok;
        cin >> ok;
        coins[ok]++;
    }
    for(int three = 0;three <= n; three++){
        for(int two = 0; two <= n; two++){
            for(int one = 0;one <= n; one++){
                int zero = n - three - one - two;
                if(zero == n) continue;
                if(three + one + two > n) continue;
                
                if(three > 0)
                dp[three][two][one] += (1.0*three/(double)n)*(double)dp[three-1][two+1][one];
                if(two > 0)
                dp[three][two][one] += (1.0*two/(double)n)*(double)dp[three][two-1][one+1];
                if(one > 0)
                dp[three][two][one] += (1.0*one/(double)n)*(double)dp[three][two][one-1];

                dp[three][two][one] += 1;
                dp[three][two][one] /= (1-(1.0*zero/(double)n));
            }
        }
    }

    cout << fixed << setprecision(10) << dp[coins[3]][coins[2]][coins[1]] << "\n";
}

