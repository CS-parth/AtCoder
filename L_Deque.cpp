int rec(int X,int Y,int flag){
    //base 
    if(X > Y){return 0;}

    //dp check
    if(dp[X][Y][flag] != -1){
        return dp[X][Y][flag];
    }
    //transition chart
    if(flag){
        int ans = -1e9;
        ans = max(arr[X]+rec(X+1,Y,!flag),arr[Y]+rec(X,Y-1,!flag));
        return dp[X][Y][flag] = ans;
    }else{
        int ans = 1e9;
        ans = min(-1*arr[X]+rec(X+1,Y,!flag),-1*arr[Y]+rec(X,Y-1,!flag));
        return dp[X][Y][flag] = ans;
    }

}
