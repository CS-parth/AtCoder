int rec(int x,int y){
    //base case 
    if(x>=n || y>=m){return 0;}
    //dp check
    if(dp[x][y] != -1){
        return dp[x][y];
    }
    //compute
    int ans = 0;

    ans = max(ans,rec(x+1,y));
    ans = max(ans,rec(x,y+1));
    if(a[x] == b[y]){
        ans = max(ans,1+rec(x+1,y+1));
    }

    return dp[x][y] = ans;
}
